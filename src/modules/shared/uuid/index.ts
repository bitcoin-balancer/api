import { v4 as uuidv4 } from 'uuid';

/* ************************************************************************************************
 *                                         IMPLEMENTATION                                         *
 ************************************************************************************************ */

/**
 * Generates an Universally Unique IDentifier (Version 4).
 * @returns string
 */
const generateUUID = (): string => uuidv4();





/* ************************************************************************************************
 *                                         MODULE EXPORTS                                         *
 ************************************************************************************************ */
export {
  generateUUID,
};
